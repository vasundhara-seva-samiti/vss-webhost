    const title = item.querySelector('.vgx-overlay h3');
    return title ? title.textContent : item.querySelector('img').alt;
  };

  function showLightbox(index) {
    if (!activeItems.length) return;
    current = (index + activeItems.length) % activeItems.length;
    const item = activeItems[current];
    const img = item.querySelector('img');
    lbImg.src = img.src;
    lbImg.alt = img.alt;
    lbCaption.textContent = getCaption(item);
    lbCounter.textContent = (current + 1) + ' / ' + activeItems.length;
  }

  function openLightbox(index) {
    lb.classList.add('open');
    document.body.style.overflow = 'hidden';
    showLightbox(index);
  }

  function closeLightbox() {
    lb.classList.remove('open');
    document.body.style.overflow = '';
  }

  items.forEach((item) => {
    item.addEventListener('click', () => {
      const index = activeItems.indexOf(item);
      if (index !== -1) openLightbox(index);
    });

    item.addEventListener('mousemove', (e) => {
      if (window.matchMedia('(max-width: 768px)').matches) return;
      const rect = item.getBoundingClientRect();
      const cx = rect.left + rect.width / 2;
      const cy = rect.top + rect.height / 2;
      const dx = (e.clientX - cx) / rect.width;
      const dy = (e.clientY - cy) / rect.height;
      item.style.transform = 'translateY(0) rotateX(' + (-dy * 5) + 'deg) rotateY(' + (dx * 6) + 'deg) scale(1.02)';
    });

    item.addEventListener('mouseleave', () => {
      if (!item.classList.contains('is-visible')) return;
      item.style.transform = 'translateY(0) scale(1)';
    });
  });

  filters.forEach((btn) => {
    btn.addEventListener('click', () => {
      filters.forEach((b) => b.classList.remove('active'));
      btn.classList.add('active');

      const filter = btn.dataset.filter;
      items.forEach((item) => {
        const show = filter === 'all' || item.dataset.category === filter;
        item.classList.toggle('hidden', !show);
      });

      activeItems = items.filter((item) => !item.classList.contains('hidden'));
      updateCount();
    });
  });

