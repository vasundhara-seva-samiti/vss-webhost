      <h2 class="anchored">Building Community Resilience to Drought, Disease, and Crisis</h2>
      <div class="section-divider"></div>
      <div class="focus-subtitle-wrapper">
<p>Western Rajasthan experiences severe drought almost every three years. The Thar Desert’s sparse and erratic rainfall means that communities are never far from the next livelihood crisis — with crops failing, water sources drying up, livestock dying of thirst and hunger, and families forced into distress migration. Following drought conditions, outbreaks of malaria and other diseases add another layer of suffering to communities already weakened by food and water insecurity.</p>
<p>For the poorest and most marginalized communities — Dalits, women, mine workers, and tribal families — these disasters are not temporary disruptions but existential threats that can undo years of fragile livelihood progress in a single season. Vasundhara Sewa Samiti’s disaster management work is built on two pillars: immediate, targeted relief for the most vulnerable during crises; and sustained community preparedness and resilience building so that communities are better able to withstand and recover from shocks. Every aspect of disaster response is community-led and community-monitored.</p>
      </div>
